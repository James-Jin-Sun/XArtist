import torch
import numpy as np
from scipy.spatial import distance_matrix

def generate_fixed_latent_points(n_samples, latent_dim):
    # Generate initial random points
    x = torch.randn(n_samples, latent_dim)

    # Normalize the points to lie on the hypersphere
    x = x / torch.norm(x, dim=1, keepdim=True)

    # Initialize variables to track the best points and maximum minimum distance
    best_points = x
    max_min_distance = 0

    # Optimize to find the most equidistant points
    for _ in range(10000):
        new_points = torch.randn(n_samples, latent_dim)
        new_points = new_points / torch.norm(new_points, dim=1, keepdim=True)
        d_matrix = distance_matrix(new_points, new_points)
        min_distance = np.min(d_matrix[np.triu_indices(n_samples, 1)])

        if min_distance > max_min_distance:
            max_min_distance = min_distance
            best_points = new_points

    return best_points

